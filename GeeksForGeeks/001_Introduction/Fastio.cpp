// It is often recommended to use scanf/printf instead of cin/cout for a fast input and output.
#include <bits/stdc++.h> //include the standard template library (STL);
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);                      //tie() is a method which simply guarantees the flushing of std::cout before std::cin accepts an input
    cout<<"Hello World\n";             //use \n instead of endl;
    return 0;
}

